package Modelo;

public class Distrito {
    private String Cod_Distr;
    private String Nom_Distr;
    public String getCod_Distr() {
        return Cod_Distr;
    }
    public void setCod_Distr(String cod_Distr) {
        Cod_Distr = cod_Distr;
    }
    public String getNom_Distr() {
        return Nom_Distr;
    }
    public void setNom_Distr(String nom_Distr) {
        Nom_Distr = nom_Distr;
    }

}
